        public static List<(float, float)> GetLatLons()
        {
            var strings = TxCoord.coordStr.Split('\n');
            var result = new List<(float, float)>();
            foreach (var coordStr in strings)
            {
                var parts = coordStr.Split(',');
                float lat = float.Parse(parts[0].Trim());
                float lon = float.Parse(parts[1].Trim());
                result.Add((lat, lon));
            }

            return result;
        }
    }


